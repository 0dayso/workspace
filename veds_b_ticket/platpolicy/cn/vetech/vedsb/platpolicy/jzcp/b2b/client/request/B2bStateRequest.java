package cn.vetech.vedsb.platpolicy.jzcp.b2b.client.request;

import javax.xml.bind.annotation.XmlRootElement;

import cn.vetech.vedsb.platpolicy.jzcp.b2b.client.VetpsPurchasB2bRequest;

@XmlRootElement(name = "request")
public class B2bStateRequest extends VetpsPurchasB2bRequest {

	/**
	 * 
	 */
	private static final long serialVersionUID = -249290810193786694L;

	private String businessNo;// 商户编号

	private String userid;// 操作人编号

	private String outOrderNo;// 外部订单编号

	public String getBusinessNo() {
		return businessNo;
	}

	public void setBusinessNo(String businessNo) {
		this.businessNo = businessNo;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getOutOrderNo() {
		return outOrderNo;
	}

	public void setOutOrderNo(String outOrderNo) {
		this.outOrderNo = outOrderNo;
	}
}
