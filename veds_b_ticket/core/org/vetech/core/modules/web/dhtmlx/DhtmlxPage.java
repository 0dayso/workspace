package org.vetech.core.modules.web.dhtmlx;

import java.util.List;
/**
 * 适配dhtmlx的分页对象
 * @author 章磊
 *
 * @param <T>
 */
public class DhtmlxPage<T> {
	//总条数
	private int total_count;
	
	//当前页
	private int pos;
	
	//数据集
	private List<T> data;

	public int getTotal_count() {
		return total_count;
	}

	public void setTotal_count(int total_count) {
		this.total_count = total_count;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}

	public List<T> getData() {
		return data;
	}

	public void setData(List<T> data) {
		this.data = data;
	}
	
	
}
