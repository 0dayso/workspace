package cn.vetech.vedsb.jp.jpddsz.taobao.model;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name="response")
public class SearchGqdResponse {
	private String state;//0成功 -1失败
	private String errMsg;//失败内容
	private List<TicketChangeDealGy> gqdList;
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getErrMsg() {
		return errMsg;
	}
	public void setErrMsg(String errMsg) {
		this.errMsg = errMsg;
	}
	public List<TicketChangeDealGy> getGqdList() {
		return gqdList;
	}
	public void setGqdList(List<TicketChangeDealGy> gqdList) {
		this.gqdList = gqdList;
	}
	
}
