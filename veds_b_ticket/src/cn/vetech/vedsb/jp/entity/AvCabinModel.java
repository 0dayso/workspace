package cn.vetech.vedsb.jp.entity;

import java.io.Serializable;

/**
 * 自动AV后比当前订单上舱位价格低的舱位集合
 */
public class AvCabinModel implements Serializable {

	/**
	 * 注释内容
	 */
	private static final long serialVersionUID = 1773400896775476240L;

	private String cw;

	private String cwNum;

	private double price;

	private double subPrice;
	
	private String fjjx;//飞机机型
	private String cfsj;//出发时间
	private String ddsj;//到达时间
	private String hzl;//航站楼
	private String hbh;//航班号

	public String getCw() {
		return cw;
	}

	public void setCw(String cw) {
		this.cw = cw;
	}

	public String getCwNum() {
		return cwNum;
	}

	public void setCwNum(String cwNum) {
		this.cwNum = cwNum;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public double getSubPrice() {
		return subPrice;
	}

	public void setSubPrice(double subPrice) {
		this.subPrice = subPrice;
	}

	public String getFjjx() {
		return fjjx;
	}

	public void setFjjx(String fjjx) {
		this.fjjx = fjjx;
	}

	public String getCfsj() {
		return cfsj;
	}

	public void setCfsj(String cfsj) {
		this.cfsj = cfsj;
	}

	public String getDdsj() {
		return ddsj;
	}

	public void setDdsj(String ddsj) {
		this.ddsj = ddsj;
	}

	public String getHzl() {
		return hzl;
	}

	public void setHzl(String hzl) {
		this.hzl = hzl;
	}

	public String getHbh() {
		return hbh;
	}

	public void setHbh(String hbh) {
		this.hbh = hbh;
	}

	
}
